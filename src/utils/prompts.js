/**
 * 引用格式提示词
 * 用于指导模型使用引用格式回答问题
 */
export const REFERENCE_PROMPT = `
请根据提供的参考资料回答问题。你的回答应该：

1. 主要基于参考资料中的信息
2. 清晰地引用参考资料的内容
3. 如果参考资料不足以回答问题，可以使用你自己的知识，但要明确指出
4. 保持回答简洁、准确、有条理
5. 忽略与问题无关的参考资料

请不要在回答中重复引用编号或明确提及"根据参考资料[X]"这样的表述，而是自然地整合信息。
`;

/**
 * 脚注格式提示词
 * 用于指导模型使用脚注格式引用知识库内容
 */
export const FOOTNOTE_PROMPT = `
请根据提供的参考资料回答问题。你的回答应该：

1. 使用脚注格式引用参考资料，例如[1]、[2]等
2. 在回答末尾列出所有引用的参考资料
3. 如果参考资料不足以回答问题，可以使用你自己的知识，但要明确指出
4. 如果参考资料中有矛盾的信息，请指出这些矛盾并给出你的分析
5. 忽略与问题无关的参考资料
6. 保持回答简洁、准确、有条理

示例格式：
问题：什么是量子计算？

回答：
量子计算是一种利用量子力学原理进行信息处理的计算方式[1]。与经典计算使用比特不同，量子计算使用量子比特（qubit）作为基本单位[2]。

参考资料：
[1] 量子计算简介 - 量子计算是利用量子力学现象如叠加和纠缠来执行计算的技术。
[2] 量子比特与经典比特 - 量子比特可以同时处于多个状态，而经典比特只能是0或1。
`;

/**
 * 翻译提示词
 * 用于指导模型进行文本翻译
 * @param {string} text 要翻译的文本
 * @param {string} targetLanguage 目标语言
 * @returns {string} 翻译提示词
 */
export const getTranslatePrompt = (text, targetLanguage) => `
请将<translate_input>标签内的文本翻译成${targetLanguage}。只需提供翻译结果，不要添加解释或其他内容。

<translate_input>
${text}
</translate_input>
`; 